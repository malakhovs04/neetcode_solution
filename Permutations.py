class Solution:
    def permute(self, nums: List[int]) -> List[List[int]]:
        self.res = []
        self.back(nums, 0)
        return self.res

    def back(self, nums, idx):
        if idx == len(nums):
            self.res.append(nums[:])
            return
        for i in range(idx, len(nums)):
            nums[idx], nums[i] = nums[i], nums[idx]
            self.back(nums, idx + 1)
            nums[idx], nums[i] = nums[i], nums[idx]
